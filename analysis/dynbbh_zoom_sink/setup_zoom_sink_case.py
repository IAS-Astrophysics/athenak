def nb_cell(source: str, cell_type: str = "code") -> dict:
    cell = {"cell_type": cell_type, "metadata": {}, "source": source.splitlines(keepends=True)}
    if cell_type == "code":
        cell["outputs"] = []
        cell["execution_count"] = None
    return cell


