def unit_scaled(vec: tuple[float, float, float], mag: float) -> tuple[float, float, float]:
    norm = math.sqrt(sum(v * v for v in vec))
    return tuple(mag * v / norm for v in vec)


def write_table() -> None:
    chi1 = unit_scaled((0.31, -0.76, 0.57), 0.95)
    chi2 = unit_scaled((-0.73, 0.22, 0.65), 0.95)
    nrows = 2201
    r1 = 0.5 * SEP
    r2 = -0.5 * SEP
    with TABLE.open("w", encoding="utf-8") as f:
        f.write(
            "# Time M1 M2 x1 y1 z1 x2 y2 z2 chi_1_x chi_1_y chi_1_z "
            "chi_2_x chi_2_y chi_2_z vx1 vy1 vz1 vx2 vy2 vz2\n"
        )
        for n in range(nrows):
            t = ZOOM_TLIM * n / (nrows - 1)
            c = math.cos(OMEGA * t)
            s = math.sin(OMEGA * t)
            x1, y1, z1 = r1 * c, r1 * s, 0.0
            x2, y2, z2 = r2 * c, r2 * s, 0.0
            vx1, vy1, vz1 = -r1 * OMEGA * s, r1 * OMEGA * c, 0.0
            vx2, vy2, vz2 = -r2 * OMEGA * s, r2 * OMEGA * c, 0.0
            row = (
                t, 0.5, 0.5, x1, y1, z1, x2, y2, z2,
                *chi1, *chi2, vx1, vy1, vz1, vx2, vy2, vz2,
            )
            f.write(" ".join(f"{v:.16e}" for v in row) + "\n")


