@dataclass(frozen=True)
class Case:
    label: str
    nx: int
    half_width: float
    boost: float
    purpose: str


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--athena", type=Path, default=REPO_ROOT / "build_tov_cpu" / "src" / "athena")
    parser.add_argument("--runner-prefix", default="")
    parser.add_argument("--run-root", type=Path, default=REPO_ROOT / "runs" / "tov_minkowski_stability")
    parser.add_argument("--summary", type=Path, default=REPO_ROOT / "tov_stability.md")
    parser.add_argument("--tlim", type=float, default=2.0)
    parser.add_argument("--hst-dt", type=float, default=0.1)
    parser.add_argument("--wall-time", default="00:20:00")
    parser.add_argument("--moderate-boost", type=float, default=0.2)
    parser.add_argument("--z4c-diss", type=float, default=0.0)
    parser.add_argument("--z4c-kappa1", type=float, default=0.0)
    parser.add_argument("--z4c-kappa2", type=float, default=0.0)
    parser.add_argument(
        "--cases",
        default="unboosted_L3_n32,unboosted_L3_n48,unboosted_L3_n64,"
        "boosted_L3_n32,boosted_L3_n48,boosted_L3_n64,"
        "unboosted_L5_n80,boosted_L5_n80",
        help="Comma-separated case names. Supported names use unboosted/boosted_L<half-width>_n<nx>.",
    )
    parser.add_argument("--dry-run", action="store_true")
    return parser.parse_args()


def parse_case(name: str, moderate_boost: float) -> Case:
    match = re.fullmatch(r"(unboosted|boosted)_L([0-9.]+)_n([0-9]+)", name)
    if not match:
        raise ValueError(f"unsupported case name: {name}")
    kind, half_width, nx = match.groups()
    boost = 0.0 if kind == "unboosted" else moderate_boost
    purpose = "boundary" if float(half_width) != 3.0 else "resolution"
    return Case(name, int(nx), float(half_width), boost, purpose)


